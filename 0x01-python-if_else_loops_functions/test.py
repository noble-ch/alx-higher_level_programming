class Decoder:
    def init(self, encoded_data):
        self.encoded_data = encoded_data

    def decode(self):
        # Split the binary string into groups of 8 bits
        binary_groups = [self.encoded_data[i:i+8] for i in range(0, len(self.encoded_data), 8)]

        # Convert each group to decimal and then to ASCII
        decoded_data = ''.join(chr(int(group, 2)) for group in binary_groups)

        return decoded_data
class ASCIIToTextConverter:
    def init(self, ascii_values):
        self.ascii_values = ascii_values

    def convert(self):
        # Convert each ASCII value to its corresponding character
        text = ''.join(chr(value) for value in self.ascii_values)

        return text

